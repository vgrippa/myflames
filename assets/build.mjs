// Bundles assets/src/runtime.ts into myflames/assets/anim-runtime.js.
// Output is an IIFE that installs `window.anim` (the primitives) —
// the rest of the teachRuntime scaffolding keeps living in Python
// (_BASE_JS) for now, since it's DOM glue, not animation.
//
// To regenerate after editing TS:  npm install && npm run build
// The resulting .js is committed so end-users never need npm.
import { build } from "esbuild";

const args = new Set(process.argv.slice(2));
const watch = args.has("--watch");

const config = {
  entryPoints: ["src/runtime.ts"],
  outfile: "../myflames/assets/anim-runtime.js",
  bundle: true,
  format: "iife",
  globalName: "MyflamesAnim",
  target: "es2020",
  platform: "browser",
  sourcemap: false,
  // Minify to keep the committed bundle small — the file is inlined
  // into every rendered HTML page, so every byte is amortized across
  // all lesson outputs.
  minify: true,
  legalComments: "inline",
  banner: {
    js: "// Auto-generated by assets/build.mjs — do not edit by hand.\n"
      + "// Source: assets/src/runtime.ts + deps (motion, d3-hierarchy, d3-shape).\n"
      + "// To regenerate: cd assets && npm install && npm run build.",
  },
  footer: {
    js: "// Merge Tier-1 primitives into the existing window.anim (installed\n"
      + "// by the hand-rolled ANIM_JS IIFE in _html.py). We copy each key\n"
      + "// from MyflamesAnim.anim without overwriting the hand-rolled ones\n"
      + "// that came first — the two surfaces are intentionally disjoint.\n"
      + "if (typeof window !== 'undefined') {\n"
      + "  window.anim = window.anim || {};\n"
      + "  for (var _k in MyflamesAnim.anim) {\n"
      + "    if (!(_k in window.anim)) window.anim[_k] = MyflamesAnim.anim[_k];\n"
      + "  }\n"
      + "  window.MyflamesTier1 = MyflamesAnim;\n"
      + "}",
  },
};

if (watch) {
  // Top-level await is fine; tsconfig target is ES2020.
  const ctx = await (await import("esbuild")).context(config);
  await ctx.watch();
  console.log("watching...");
} else {
  await build(config);
  console.log("built myflames/assets/anim-runtime.js");
}
