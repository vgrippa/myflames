    def _run(self, *args, input_text=None):
        cmd = [self.PYTHON, "-m", "myflames"] + list(args)
        result = subprocess.run(
            cmd,
            input=input_text,
            capture_output=True,
            text=True,
            cwd=REPO_DIR,
        )
        return result

