def _load(path):
    with open(path, encoding="utf-8") as f:
        return f.read()


def _tree_depth(node, depth=1):
    if not node.get("children"):
        return depth
    return max(_tree_depth(c, depth + 1) for c in node["children"])


def _is_valid_svg(text):
    """Returns True if text looks like a well-formed SVG document."""
    stripped = text.strip()
    return (
        stripped.startswith("<?xml") or stripped.startswith("<svg")
    ) and "</svg>" in stripped


