def _make_output_test(fixture_path, renderer_name):
    """Factory: returns a test method for one (fixture, renderer) pair."""
    def test_method(self):
        text = _load(fixture_path)
        root = parse_explain(text)
        fname = os.path.basename(fixture_path)

        if renderer_name == "flamegraph":
            from myflames.flamegraph import folded_to_svg
            entries = list(build_flame_entries(root))
            lines = []
            for path, t in entries:
                ms = int(t * 1000 + 0.5)
                ms = max(ms, 1) if len(path) == 1 else ms
                if ms > 0:
                    lines.append(";".join(path) + " " + str(ms))
            if not lines:
                self.skipTest(f"{fname}: all-zero self-time, no flamegraph data")
            svg = folded_to_svg("\n".join(lines), title="Test")
        elif renderer_name == "bargraph":
            svg = render_bargraph(root)
        elif renderer_name == "treemap":
            svg = render_treemap(root)
        elif renderer_name == "diagram":
            svg = render_diagram(root)
        elif renderer_name == "tree":
            svg = render_tree(root)
        else:
            self.fail(f"Unknown renderer: {renderer_name}")

        self.assertIsInstance(svg, str, f"{fname}/{renderer_name}: expected str output")
        self.assertTrue(svg.strip(), f"{fname}/{renderer_name}: output is empty")
        self.assertTrue(
            _is_valid_svg(svg),
            f"{fname}/{renderer_name}: output is not valid SVG\n"
            f"(starts with: {svg[:80]!r})",
        )

    test_method.__name__ = (
        f"test_{renderer_name}_{os.path.basename(fixture_path).replace('-', '_').replace('.json', '')}"
    )
    test_method.__doc__ = f"{renderer_name} on {os.path.basename(fixture_path)}"
    return test_method


class TestOutputsMeta(type):
    """Metaclass that dynamically generates test methods for all fixture × renderer combos."""

    def __new__(mcs, name, bases, namespace):
        cls = super().__new__(mcs, name, bases, namespace)
        renderers = ["flamegraph", "bargraph", "treemap", "diagram", "tree"]
        for fixture in ALL_FIXTURES:
            for renderer in renderers:
                method = _make_output_test(fixture, renderer)
                # Ensure unique method name (truncate if very long)
                method_name = method.__name__[:120]
                setattr(cls, method_name, method)
        return cls


